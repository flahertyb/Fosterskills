<p class="main-text"><strong style="font-weight: bold">Statistics Show</strong></p>
<p class="main-text">250,000 youth enter into the foster care system, yearly. 40% of former foster youth have repeated one or more grades; only 20-30% graduate high school by age 19. 52% read below grade level [compared to 38% of the overall population]. 37% of former foster youth experienced homelessness after age 18.</p>
<p class="main-text">The statistics are sobering. But empirical research illustrates that<strong style="font-weight:bold;"> social supports act a buffer against the negative effects of neglect, and both physical and mental abuse.</strong> Young adults who were maltreated as youth have been found to achieve better psychological adjustment when they have high levels of current perceived social support.</p>
<p class="main-text"><strong style="font-weight:bold;">The challenges facing foster children are abundant, but we believe in change. We believe in achievement. We believe in perseverance, support, and high expectations. We have the opportunity to shape lives, to build futures, to produce valuable, capable, productive citizens.</strong></p>
<p class="main-text">We know these young people have immense potential - and it is our goal to unlock it. We know they can achieve - and we want to help them get there. We believe in education and the value of practical life skills - and we intend to keep our expectations high. We know that the smallest effort can have the greatest impact - and we are willing to put in the effort.</p>

