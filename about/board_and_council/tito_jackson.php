<b><strong style="font-weight:bold; font-size:medium; line-height:18px">Tito Jackson</b></strong><strong style="font-size:medium; line-height:18px">, Boston City Councillor</strong>
<br><br>
Tito H. Jackson is the Boston City Councillor for District 7. He was elected with 82% of the vote during a Special Election. Councillor Jackson was raised in the Grove Hall section of Roxbury and is a proud life-long resident of District 7, which includes all of Roxbury and parts of the South End, Dorchester, Jamaica Plan and the Fenway.
<br><br>
Mr. Jackson is the son of Herb and Rosa Jackson, two beloved community activists. He graduated from the University of New Hampshire with a Bachelor of Arts degree in History. On campus, Councillor Jackson was Student Body President and well-known for his commitment to civic engagement and social justice. Upon graduation, he continued his involvement with academic-related programs as a member of the UNH Alumni Board and a graduate of the Dunk The Vote Leadership Institute Initiative for Diversity in Civic Leadership. The Councillor is an active leader in youth development programs such as Citizen Schools and Anytown/The Leadership Initiative.
<br><br>
Councillor Jackson is a much sought after public speaker at career days, motivational seminars and community events. In this capacity, he addresses youth development issues, such as how to better motivate young people to pursue careers in STEM (Science Technology Engineering and Math) Education.  
<br><br>
Mr. Jackson spent more than 10 years as a sales and marketing professional.  He won numerous awards and honors during that time. Through that experience, the Councillor developed extensive knowledge of the healthcare, technology and government sectors.  
<br><br>
In 2007, Councillor Jackson became the Industry Director for Information Technology at Governor Deval Patrick's Executive Office of Housing and Economic Development where he focused his energies on creating and sustaining jobs within Massachusetts and helping local businesses grow. As a member of the Patrick Administration, Councillor Jackson brought 2,500 jobs to Massachusetts. Later, he became the political director on Governor Patrick's successful re-election campaign in 2010. The Councillor now sits on the boards of Heading Home and the Global Citizens Circle. 
<br><br>
As City Councillor, Mr. Jackson plans to work for the economic revitalization of District 7, starting with The Dudley Plan. He knows that the key to economic recovery is entrepreneurship and facilitating a range of employment opportunities.  Providing our children with world-class schools is also high on the Councillor's agenda.
<br><br>
Councillor Jackson is now the Chair of the Committee of Arts, Film, Humanities and Tourism, and Vice Chair of the Post Audit & Oversight Committee. He also serves on the Committees of City and Neighborhood Services and Veteran Affairs, Education, Environment and Health, Labor, Youth Affairs and Human Rights, Ways and Means, Women & Healthy Communities, and the Special Committee on Asthma. 
