<b><strong style="font-weight:bold">Why Foster Skills?</b></strong> <br>Nicole believes that students need high expectations and the ability to participate in the making of their own education in order to unlock their own potential. Students, particularly foster students, need to know that the world around them is flexible, not rigid. Their world is theirs to change if they can learn how to push back.
<br><br><b><strong style="font-weight:bold">Outside of Foster Skills:</b></strong> <br>Nicole attended Northeastern University as a Reggie Lewis Scholar, and graduated with a degree in Architecture and International Affairs. In 2009, she received the School of Architecture Award for Highest Design Achievement or Promise in the Class of 2011. During her time at Northeastern, she volunteered her time at more than 30 open houses and other school events. She was also a teaching assistant, and developed and taught a series of lectures on computer skills for design representation. As a result of her dedication, the School of Architecture created the Student Engagement Award specifically for her. Her work as an ambassador of the School led to things like involvement in the planning of several architectural conferences, a role as student editor of a research book on rowhouse prototypes, participation in the faculty search process for a new degree program in Urban Landscape, and inclusion in meetings of the Advisory Council of the School of Architecture.
<br><br>Nicole has worked on a wide range of projects at Hacin + Associates, an award-winning architecture and design firm in Boston's South End. Her project experience ranges from large-scale commercial housing to high-end interior design, and from executive summaries to architectural photoshoots. In addition, she has done all of the graphic design and production for a wide range of digital and print marketing materials, as well as custom wallpapers and installations of graphic artwork - Nicole was featured in Boston Magazine for her installation at the Boston Concept Home. Nicole also does freelance graphic design and branding, and loves to rescue old furniture. You can view her work on her website at <a href="http://www.nicolefichera.com">nicolefichera.com</a>. 
<br><br><b><strong style="font-weight:bold">Fun fact:</b></strong> <br>Nicole loves to sing, and makes scrumptious tomato sauce.
