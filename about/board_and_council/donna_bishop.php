<br><br>
Donna M. Bishop received her PhD in Criminal Justice in 1982 from the State University of New York at Albany and taught for two decades in the Florida University System before joining the faculty of the College of Criminal Justice at Northeastern University in 1999. For nearly three decades, her research and scholarship have focused primarily on juvenile justice and youth policy. She has written two books-including Juvenile Crime, Juvenile Justice (Oxford, in press)-and over sixty articles and monographs. Her major works focus on racial and gender inequities in justice processing; juvenile law reform; and juvenile transfer policy and practice. Professor Bishop has also been very active in both the Academy of Criminal Justice Science and the American Society of Criminology. She was elected Executive Councilor for the American Society of Criminology and has chaired and served on multiple of its committees. She is a former editor of Justice Quarterly and currently serves on the editorial boards of six major journals in criminology and criminal justice.
<br><br>
Professor Bishop has frequently taken her academic research into the policy arena. She has presented her research findings to legislative bodies; judicial conferences; prosecutorial conferences; public defender and sentencing advocacy groups; juvenile task forces and commissions; and the National Academy of Sciences. She has contributed to amicus curiae briefs submitted to the U.S. Supreme Court, and has testified as an expert witness in several cases involving waiver of juvenile defendants to criminal court for prosecution and punishment as adults. Â She is also a member of the Advisory Council of the Washington-based Campaign for Youth Justice and consults with numerous other youth advocacy groups.
