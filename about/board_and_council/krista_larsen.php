Attorney Krista M. Larsen joined Northeastern as an adjunct professor in 2007 and now maintains a full time course load.  Her teaching focus is the Due Process course which focuses on the 4th, 5th, and 6th Amendments.  
<br><br>
Attorney/Professor Larsen went to Suffolk University Law School where she earned her Juris Doctor (cum laude) in 1998.  In 2003, she was followed by the Boston Globe for an article reporting on funding for court appointed attorneys.  In 2005, she was named one of Massachusetts Super Lawyers.  Attorney/Professor Larsen received her Bachelor of Science (magna cum laude) in Criminal Justice with a minor in Business Administration from Northeastern University in 1995.  Attorney/Professor Larsen co-op's included the Drug Enforcement Administration and the Brookline Police Department.  Her success at these co-op's earned her the Cooperative Education Award in 1995.
<br><br>
Attorney/Professor Larsen specializes in criminal defense and appeals while maintaining a general practice.
