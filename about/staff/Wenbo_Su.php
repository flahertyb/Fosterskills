<b><strong style="font-weight:bold">Wenbo Su, Exective Director</b></strong><br><br>

Moving from China to the US at an early age, Wenbo has benefited tremendously from the many opportunities education makes available. He has become passionate about helping others, especially youth, reach their goals and potential. Throughout high school and college, Wenbo has provided individual and group instruction in a variety of subjects to students with diverse ages and backgrounds. He has seen first-hand the effectiveness of encouragement and good information in helping kids thrive. A firm believer in the importance of non-profit organizations in solving social problems, Wenbo believes that Foster Skills can provide youth with the skills and self-confidence to succeed. 
<br><br>Wenbo is responsible for teaching portions of the Foster Skills curriculum and managing volunteers. As an experienced tutor and instructor, Wenbo will work directly with foster youth to develop essential academic and life skills. He will also help coach other instructors and volunteers to be more effective. Finally, Wenbo will aid in the recruitment and retention of volunteers for the organization.
<br><br>Wenbo is the author of "High School Success and College Application" (under pen name Anthony Freeman for marketing and privacy purposes), which is available on Amazon.com. While attending Northeastern University on full merit scholarship, he completed three internships, at GEICO (management internship), Rothstein Kass (accounting), and GE Capital (Financial Management Program). Passionate about the field of investing, Wenbo spends a great deal of time learning the craft, managing investments for family members, and aspires to become a professional investor in the future. 
