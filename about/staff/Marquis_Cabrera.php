<br>
<b><strong style="font-weight:bold">Why Foster Skills?</b></strong> <br>The odds facing children in foster care are grave. Having been through the foster care system, Marquis believes it is his duty to create opportunities for foster youth. Marquis says, "I founded Foster Skills to create collaborations between all parties and persons working to tackle issues facing children in foster care - and more importantly develop an effective team equipped to help foster youth achieve the American Dream."
<br><br><b><strong style="font-weight:bold">Outside of Foster Skills:</b></strong> <br>Marquis Cabrera is a native of New York, New York. He studied Criminology with a concentration in Juvenile Law and Policy at Northeastern University, earning a Bachelors of Science magna cum laude.<br><br>
While at Northeastern, Marquis was very involved in the lives of young people. He was an after school club leader and specialist at the Agassiz Elementary School, and he was a mentor to the Martin Luther King Summer Scholars and participants of Judicial Youth Corps Program. In 2009, Dr. Chiaradonna asked Marquis to be her Teacher's Assistant because of his youth involvement and without hesitation he agreed. He researched and presented on the affects narcissistic attitudes and the inner child have on youth development.
<br><br>
Marquis was elected Pledge Class President of the The Kappa Sigma Fraternity - and after his initiation he was elected to the post of Brothers In Action. He was the campaign manager for Rob Ranley, who was elected 2007-2008 NU Student Body President by way of direct elections. He was the Founding President of the Pre-Law Chapter of Phi Alpha Delta at Northeastern University, and he Co-founded the Boston Dialectical Society. His strong track record of leadership led to his becoming one of 100 Most Influential Seniors in the 2011 graduating class. He, too, was a winner of the Senior Achievement Award and The Terry Mena Social and Political Justice Award; and student speaker for the School of Criminology and Criminal Justice's Class of 2011.  
<br><br>Marquis has worked in government, business, and nonprofit sectors. He built a computer program that created guest and student IDs that scanned to increase school district security for The Enlarged City School District of Middletown. He also worked in the HP (HR) department at City Year, and he was integral to the recruitment business process.  He served as an operations and legal policy intern in the White House Office of the Chief of Staff. There, he worked on economic, energy, privacy, and fiscal policy issues. His long-term project was leading White House efforts to help the Office of Management and Budget (OMB) stand up a series of online seminars to train sub- and primary-recipients of Recovery Act funding to comply with the reporting requirements of the M-09-21 guidance. He was also a consultant for three years at Wayfair working to drive revenue, build brand recognition and develop internal processes and structures. He currently sits on the three for-profit boards and two non-profit boards. 
<br><br>
In 2012, he will publish his first book, "<u>Foster Marque Weem</u>," a fiction book that provides a message to the world that we need to create more opportunities. 
