<br><br><b><strong style="font-weight:bold">Ms. Snodgrass is the founder of <a class="main-text" href="http://www.reachforyou.org">REACH</a>, an after school learning program for high school aged youth in foster care.</b></strong> Ms. Snodgrass currently has two working interventions and one charter school design specifically for foster youth, and former foster youth to become empowered and successful. Ms. Snodgrass also currently serves as the Communications Board Member for Foster Care Alumni of America, MA Chapter, and well as three other advisory boards related to youth and families: Together We Rise, Gift for a Child, and Orangewood Children's Foundation.</p>
