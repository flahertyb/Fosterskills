<b><strong style="font-weight:bold">Dorota Sernicki<br>Senior Vice President of Accounting</b></strong><br><br><br>

Dorota was born in Connecticut in 1989. She was raised in a bicultural environment, and is fluent in Polish, competent in Ukranian and academically proficient in Spanish. At a young age, Dorota was already a strong proponent of education. Her involvement in her hometown community included volunteering as a religious education teacher as well as supporting education in the arts as a private violin instructor.  
<br><br>Dorota is responsible for ensuring compliance with government regulations and accounting standards with respect to financial accountability of the organization. As of now, Dorota has established internal controls for accounting within Foster Skills and is managing the accounting system. Dorota will work with CPAs to ensure government (IRS) compliance until she becomes an official CPA next year. 
<br><br>Dorota has developed a strong work ethic and an acuteness to financial detail through her employment at the Boston College Associate Treasurer's Office. As a 2011 Boston College Graduate from the Carroll School of Management, Dorota has become aware of the currently financial environment and the need for high ethical standards and integrity in the accounting practice. 
