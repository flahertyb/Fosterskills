<br><br>School was always my passion, I struggled in a few areas; however, it was clear to everyone that I was very bright. I had a deep-running passion for education. I used it as an outlet to feel "normal." During my senior year of high school I was announced as my senior class' Valedictorian and also awarded a full scholarship to Northeastern University in Boston, MA. I'm graduating this Spring with a BA in Organizational Communication. My first co-op was with Northeastern's STEM (Science, Technology, Engineering, and Mathematics) program. I'm currently in Melbourne, Australia on coop for Northeastern's "N.U.in program" as an International Student Adviser. I'm also in the midst of applying for many graduate school programs.

<br><br><b><strong style="font-weight:bold">I am a product of the foster care system. I am a living testimony that your circumstances do not make you, but you make your circumstances. I am the end result of dedication, motivation, and unbreakable dreams."</strong></b>
