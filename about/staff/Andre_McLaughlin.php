<br><b><strong style="font-weight:bold">Why Foster Skills?</b></strong>
<br>
It took two worlds to build one person along with a great deal of family support and love. Andre is a strong believer in providing children with essential skills to excel in life. More specifically Andre understands exactly how difficult life can be for children that lack the appropriate amount of family support. Andre was born and raised in Boston Massachusetts by his mother and grandparents however attended Newton Public Schools by way of The Metropolitan Council for Educational Opportunity (METCO). Andre is a strong believer in providing less fortunate children with an opportunity to surpass their environments expectations. He is thankful for the opportunity he was given and has always sought out ways to give back to the community either through experience or by joining similar causes with similar agendas. 

<br><br><b><strong style="font-weight:bold">Outside of Foster Skills:</b></strong> <br>

Andre began mentoring children at the Local Roxbury Y.M.C.A. by way of afterschool and summer programs. In addition he became known for mentoring youths on the basketball court. At Newton South High School Andre was a Peer Mentor for 2 years who excelled in the classroom and on the basketball court. His academic success along with athletic success gave him the opportunity to Attend Wheaton College in Norton Massachusetts. While at Wheaton College Andre majored in Psychology and had a strong focus in gender relations as well as economics. Throughout his college career Andre periodically worked at the Bird Street Community Center as a Teacherâ€™s Assistant; where he worked with adolescents to help build their confidence within their classrooms. 
After Wheaton Andre found himself working for an ecommerce company in Boston, Wayfair LLC (Formerly CSN Stores); it was during this time that Andre Met Marquis. The two became friends joking now and again about sports and company happenings; little did Andre know that Marquis would give him an opportunity to join a greater cause. 


