<br><br>
Elizabeth Berges lived in Norton, Zimbabwe from 2002-2003 while working as an Educational Consultant to Tsungirirai Orphan Care Centre.  She fundraised enough money to build and stock a library for the centre, where she focused most of her energy during her time in Zimbabwe. She also wrote and managed a grant from UNICEF to strengthen youth groups, and created a monitoring and evaluation tool called "Station Day" that has been highlighted by Catholic Relief Services as a best practice. Upon returning to the United States, she and Arden O'Donnell founded Coalition for Courage in an effort to continue the work that was begun in Zimbabwe. She attended Mount Holyoke College for her undergraduate years and then Harvard Graduate School of Education for a Master's degree, where she focused on adolescent risk and resilience. Liz now teaches seventh and eighth grade English Language Arts at the Lyndon Pilot School in Boston.
