<b><strong style="font-weight:bold">Why Foster Skills?</b></strong> <br>Bart has always had an affinity for helping, teaching, and understanding. Bart met Marquis at the Northeastern Dialectical Society, a debate group at Northeastern, in early 2011.  They began discussing the concept of a web portal of contacts and resources designed for foster children, inspired by the work of students in Greg Goodale's advocacy workshop class.
