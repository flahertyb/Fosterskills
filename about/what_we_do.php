<h2 class="main-text">Foster Skills strives to enrich the lives of foster youth by:</h2>

<p class="main-text" style="margin-top:25px;"><strong style="font-weight:bold;">1. Providing emotional development and practical, transferable life skills programs.</strong>
<br>
Through group peer-to-peer relationships, college students teach foster youth necessary life skills, such as: self-management, modeling, community building, and life skills. We phase these over the course of several years, coordinating the lesson plans to unfold as students mature.</p>

<p class="main-text" style="margin-top:25px;"><strong style="font-weight:bold;">2.  Bridging connections with like organizations, and connecting youth to resources.</strong><br>
Finding resources for children in foster care is often difficult due to the lack of communication, thereby leading to a fragmented system of care. Foster Skills has developed working relationships and partnerships with many local organizations to show the power of collaboration, and to connect foster youth to these community partners and resource organization.</p>

<p class="main-text" style="margin-top:25px;"><strong style="font-weight: bold">3.  Creating widespread awareness about the issues facing foster youth.</strong><br>
Issues facing foster children need to be brought to the national platform, so best practices can be formed, and champions can be recruited to the cause. Foster Skills helps youth share their stories across the country.</p>


